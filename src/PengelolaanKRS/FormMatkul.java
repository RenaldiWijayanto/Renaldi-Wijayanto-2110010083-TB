    private void jtMatkulMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtMatkulMouseClicked
        // TODO add your handling code here:
        
        btnTambah.setEnabled(false);
        
        int pilih = jtMatkul.getSelectedRow();
        String kd_matkul = jtMatkul.getValueAt(pilih, 0).toString();
        String nama_matkul = jtMatkul.getValueAt(pilih, 1).toString();
        String sks = jtMatkul.getValueAt(pilih, 2).toString();
        String semester = jtMatkul.getValueAt(pilih, 3).toString();
        String dosen = jtMatkul.getValueAt(pilih, 4).toString();
        
        tfKode.setText(kd_matkul);
        tfMatkul.setText(nama_matkul);
        tfSks.setText(sks);
        tfSemester.setText(semester);
        tfDosen.setText(dosen);
    }//GEN-LAST:event_jtMatkulMouseClicked

