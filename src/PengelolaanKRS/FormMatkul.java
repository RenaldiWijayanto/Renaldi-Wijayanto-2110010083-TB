    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String kd_matkul =tfKode.getText();
        String matkul = tfMatkul.getText();
        String sks = tfSks.getText();
        String semester = tfSemester.getText();
        String dosen = tfDosen.getText();
        
        if (tfKode.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "id mata kuliah tidak boleh kosong");
        }else if (tfMatkul.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "mata kuliah tidak boleh kosong");
        }else if (tfSks.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "sks tidak boleh kosong");
        }else if (tfSemester.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "semester tidak boleh kosong");
        }else if (tfDosen.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "dosen tidak boleh kosong");
        }else{
        
        try {
            new matkul().insert(kd_matkul, matkul, sks, semester, dosen);
            JOptionPane.showMessageDialog(this, "Data Berhasil Di Masukkakn");
            initTabel();
            kosongkan_form();
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Gagal Dimasukkan Karena "+e.getMessage());
        }
    }
    }//GEN-LAST:event_btnTambahActionPerformed

