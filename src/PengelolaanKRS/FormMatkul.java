    // Untuk mencari data Mata Kuliah dengan nama matkul menggunakkan keyreleased
    private void tfCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfCariKeyReleased
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("KD MATKUL");
        model.addColumn("MATA KULIAH");
        model.addColumn("SKS");
        model.addColumn("SEMESTER");
        model.addColumn("DOSEN");
        
        try{
            String sql = "SELECT * FROM matkul WHERE nama_matkul LIKE '%"+tfCari.getText()+"%'";
            java.sql.Connection conn=(Connection)Koneksi.koneksiDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
            }
            jtMatkul.setModel(model);
            
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
    }//GEN-LAST:event_tfCariKeyReleased

