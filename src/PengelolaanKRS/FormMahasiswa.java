    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String nim =tfNIM.getText();
        String nama = tfNama.getText();
        String alamat = taAlamat.getText();
        String jk = cbJK.getSelectedItem().toString();
        String jurusan = cbJurusan.getSelectedItem().toString();
        String studi = cbStudi.getSelectedItem().toString();
        
        if (tfNIM.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "nim tidak boleh kosong");
        }else if (tfNama.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "nama tidak boleh kosong");
        }else if (taAlamat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "alamat tidak boleh kosong");
        }else if (cbJK.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "jenis kelamin harus dipilih");
        }else if (cbJurusan.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, " jurusan harus dipilih");
        }else if (cbStudi.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, " Studi harus dipilih");
        }else{
        try {
            new mahasiswa().insert(nim, nama, alamat, jk, jurusan, studi);
            JOptionPane.showMessageDialog(this, "data berhasil dimasukkan");
            initTabel();
            kosongkan_form();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "data gagal dimasukkan karena "+e.getMessage());
        }
      }
    }//GEN-LAST:event_btnTambahActionPerformed
    
